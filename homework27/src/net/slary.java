package net;

public class slary {
	private String slary;
	private int slarynum;
	public slary(String slary, int slarynum) {
		super();
		this.slary = slary;
		this.slarynum = slarynum;
	}
	public String getSlary() {
		return slary;
	}
	public void setSlary(String slary) {
		this.slary = slary;
	}
	public int getSlarynum() {
		return slarynum;
	}
	public void setSlarynum(int slarynum) {
		this.slarynum = slarynum;
	}
	
}
